// Bootstrap the USB StorageService instance: hand it the USB stick's block service
// ("USBBLOCK", served by the xhci driver over the Bulk-Only Transport and routed up in
// DeviceManager's phase 2), which it mounts as the writable FAT vol://usb volume, then
// mint its service channel ("SERVE"); the client end is kept in `*usb_client` and later
// handed to the shell. The block handle is 0 when the xhci driver never came up (no
// controller, or the driver failed) - the instance must still come up, so a dead-peer
// stand-in channel is handed over instead: the removable FAT backing mounts lazily and
// every probe of the dead channel fails like absent media, so vol://usb simply shows
// as unavailable rather than failing the boot chain (the shell depends on this
// instance).
unsafe fn bootstrap_usb_storage(manager_side: u64, block5_client: u64, usb_client: &mut u64) -> bool {
	unsafe {
		let block: u64 = if block5_client != 0 {
			block5_client
		} else {
			let (dead_server, dead_client): (u64, u64) = match channel() {
				Some(pair) => pair,
				None => return false,
			};
			close(dead_server);
			dead_client
		};
		if !send_blocking(manager_side, b"USBBLOCK", block) {
			return false;
		}
		bootstrap_serve(manager_side, usb_client)
	}
}

// Hand NetworkService the net driver's frame channel ("FRAMES", routed up from the
// virtio-net driver via DeviceManager - it moves frames over it) and the channel
// its clients reach it on ("SERVE"). The service-channel client end is kept in
// `*net_client` and later transferred to the shell for the `ip`/`ping`/`nslookup`
// commands.
unsafe fn bootstrap_network_service(manager_side: u64, net_frames: u64, config_client: u64, net_client: &mut u64) -> bool {
	unsafe {
		if !send_blocking(manager_side, b"FRAMES", net_frames) {
			return false;
		}
		// The config tree's client (the `net.arp-cache` policy), minted fresh - the
		// service depends on config_service, so the tree serves by now. Handle 0
		// tells the service to fall back to its compiled-in default.
		let cfg: u64 = match service_connect(config_client) {
			Some(c) => c,
			None => 0,
		};
		if !send_blocking(manager_side, b"CONFIG", cfg) {
			return false;
		}
		bootstrap_serve(manager_side, net_client)
	}
}

// Hand TimeService its own NetworkService client (for the SNTP query) and the channel
// its clients reach it on. The network client is minted from the multi-client
// `network.open()` on the supervisor's NetworkService channel and transferred as
// "NET"; then "SERVE" transfers one end of a fresh service channel, the other kept in
// `*time_client` and later handed to the shell for the `date` command and `log`
// wall-clock rendering. (TimeService depends on network_service, so `net_client` is
// already set by the time this runs.)
unsafe fn bootstrap_time_service(manager_side: u64, net_client: u64, time_client: &mut u64) -> bool {
	unsafe {
		let mut net = network::Client::new(ChannelTransport { chan: net_client });
		let time_net: u64 = match net.open() {
			Some(Ok(h)) => h,
			_ => return false,
		};
		if !send_blocking(manager_side, b"NET", time_net) {
			return false;
		}
		bootstrap_serve(manager_side, time_client)
	}
}

// Hand AudioService the virtio-snd driver's control channel ("SND" - a 0 handle when
// no sound device is present, routed up from the snd driver via DeviceManager) and the
// channel its clients reach it on ("SERVE"). The service-channel client end is kept in
// `*audio_client` and later handed to the shell (and to ConsoleService as a factory)
// for the `beep` command. (AudioService depends on device_manager, so `snd_client` is
// already set by the time this runs; it is 0 when there is no sound device, and
// AudioService then answers `beep` with a not-found error.)
unsafe fn bootstrap_audio_service(manager_side: u64, snd_client: u64, audio_client: &mut u64) -> bool {
	unsafe {
		if !send_blocking(manager_side, b"SND", snd_client) {
			return false;
		}
		bootstrap_serve(manager_side, audio_client)
	}
}

// Hand ConsoleService the client end of a fresh console channel over "CLIENT" (VT 1's
// terminal: the shell writes its output to it and reads its keystrokes from it), then
// a *factory* connection to every multi-client service plus a read-only view of the
// init package. ConsoleService is the session spawner: when it opens an additional
// virtual terminal it mints a fresh per-VT client from each factory (`service_connect`
// / `network.open`) and spawns that VT's shell with the full capability set, so every
// VT runs a fully-capable shell over its own independent service connections. The
// factories are independent connections (not the supervisor's own clients or VT 1's),
// so minting from them never crosses the supervisor's lifecycle traffic. ConsoleService
// maps the framebuffer itself (the kernel console then stops drawing) and attaches to
// the kernel console input for keys.
unsafe fn bootstrap_console_service(manager_side: u64, storage_client: u64, log_client: u64, device_client: u64, process_client: u64, config_client: u64, net_client: u64, gpu_client: u64, time_client: u64, audio_client: u64, session_client: u64, perm_client: u64, pointer_console: u64, console_client: &mut u64, console_control: &mut u64) -> bool {
	unsafe {
		let (service_end, client_end): (u64, u64) = match channel() {
			Some(pair) => pair,
			None => return false,
		};
		if !send_blocking(manager_side, CAP_CLIENT, service_end) {
			return false;
		}
		*console_client = client_end;
		// VT 1's control channel: the console end goes to ConsoleService now, the shell end
		// is kept for the shell's own bootstrap (it starts later in the boot order).
		let (control_console, control_shell): (u64, u64) = match channel() {
			Some(pair) => pair,
			None => return false,
		};
		if !send_blocking(manager_side, CAP_CONTROL, control_console) {
			return false;
		}
		*console_control = control_shell;
		// A factory connection per serve_multi service, minted with `service_connect`.
		if !send_factory(manager_side, CAP_FSTORAGE, storage_client) {
			return false;
		}
		if !send_factory(manager_side, CAP_FLOG, log_client) {
			return false;
		}
		if !send_factory(manager_side, CAP_FDEVICE, device_client) {
			return false;
		}
		if !send_factory(manager_side, CAP_FPROCESS, process_client) {
			return false;
		}
		if !send_factory(manager_side, CAP_FCONFIG, config_client) {
			return false;
		}
		if !send_factory(manager_side, CAP_FTIME, time_client) {
			return false;
		}
		if !send_factory(manager_side, CAP_FAUDIO, audio_client) {
			return false;
		}
		// The SessionService factory, so ConsoleService can mint a fresh per-VT session for
		// each additional virtual terminal it spawns.
		if !send_factory(manager_side, CAP_FSESSION, session_client) {
			return false;
		}
		// The PermissionManager factory, so ConsoleService can mint a fresh per-VT launcher
		// client for each shell it spawns.
		if !send_factory(manager_side, CAP_FPERM, perm_client) {
			return false;
		}
		// NetworkService is multi-client through its own typed `open`, not serve_multi.
		let mut net = network::Client::new(ChannelTransport { chan: net_client });
		let net_fac: u64 = match net.open() {
			Some(Ok(h)) => h,
			_ => return false,
		};
		if !send_blocking(manager_side, CAP_FNET, net_fac) {
			return false;
		}
		// The gpu driver's display channel (0 when there is no virtio-gpu device, e.g.
		// under test - ConsoleService then falls back to the boot framebuffer).
		if !send_blocking(manager_side, CAP_GPU, gpu_client) {
			return false;
		}
		// The pointer-forward channel from InputService (0 when no pointer device this
		// boot): ConsoleService reads raw pointer events off it to drive selection,
		// scrollback, and SGR mouse reports.
		if !send_blocking(manager_side, CAP_POINTER, pointer_console) {
			return false;
		}
		send_ready(manager_side)
	}
}

// Mint an independent factory connection to a serve_multi service and transfer it to
// ConsoleService under `tag`. The factory is a fresh client connection, so the
// session spawner can mint per-VT clients from it without racing other holders.
unsafe fn send_factory(manager_side: u64, tag: &[u8], root: u64) -> bool {
	unsafe {
		match service_connect(root) {
			Some(fac) => send_blocking(manager_side, tag, fac),
			None => false,
		}
	}
}

// Spawn the managed watchdog canary from the init package, keeping its control channel
// (the canary serves directly on its bootstrap channel, so the supervisor's end is both
// the bootstrap peer and the control channel). Returns (Process, control), or (0, 0) on
// failure. Waits for the canary's "online" report so the caller knows it is up before
// exercising the restart and watchdog paths against it. The canary is raw-spawned from
// the package (not loaded from the volume) because the supervisor exercises it after
// stopping DeviceManager - which drops virtio_blk, so the system volume is unavailable.
unsafe fn spawn_canary(package: &Package, buf: &mut [u8]) -> (u64, u64) {
	unsafe {
		let elf: &[u8] = match package.lookup(b"watchdog_probe") {
			Some(e) => e,
			None => return (0, 0),
		};
		let (ctrl, probe_side): (u64, u64) = match channel() {
			Some(pair) => pair,
			None => return (0, 0),
		};
		let proc: i64 = spawn(elf, probe_side);
		if proc < 0 {
			return (0, 0);
		}
		match recv_blocking(ctrl, buf) {
			Received::Message { .. } => (proc as u64, ctrl),
			Received::Closed => {
				close(proc as u64);
				(0, 0)
			}
		}
	}
}

// Restart the managed canary per the restart policy: record what took it down, drain
// and release its dead endpoints, then - unless the restart budget is spent - back off
// (longer after repeated failures) and respawn, charging one restart. Returns true if a
// replacement is running, false if the budget is exhausted (the caller escalates). The
// canary stands in for the policy a real service restart would follow.
unsafe fn restart_canary(package: &Package, proc: &mut u64, ctrl: &mut u64, sup: &mut Supervised, failure: Failure, budget: u32, park: u64, buf: &mut [u8]) -> bool {
	unsafe {
		sup.failure = failure;
		// Reap the old endpoints so the dead process is fully gone before its replacement.
		drain_closed(*ctrl, buf);
		if *ctrl != 0 {
			close(*ctrl);
			*ctrl = 0;
		}
		if *proc != 0 {
			close(*proc);
			*proc = 0;
		}
		// Spend from the restart budget; once exhausted, escalate rather than restart again.
		if sup.restarts >= budget {
			return false;
		}
		// Back off before the respawn, scaled by the attempt count. A bounded one-shot
		// sleep, so the test scheduler still advances it deterministically.
		sleep_ticks(park, RESTART_BACKOFF_TICKS * (sup.restarts as u64 + 1));
		let (new_proc, new_ctrl): (u64, u64) = spawn_canary(package, buf);
		if new_proc == 0 {
			return false;
		}
		*proc = new_proc;
		*ctrl = new_ctrl;
		sup.restarts += 1;
		true
	}
}

// Drain a channel until its peer is gone, discarding any queued messages. Used to wait
// out a dying process so its control channel is fully closed before it is replaced.
unsafe fn drain_closed(channel: u64, buf: &mut [u8]) {
	unsafe {
		if channel == 0 {
			return;
		}
		loop {
			match recv_blocking(channel, buf) {
				Received::Message { .. } => {}
				Received::Closed => return,
			}
		}
	}
}

// ---- the transparent-restart broker (the Resolver) ---------------------------------
//
// The durable reference a client holds to a service is the capability NAME, not the
// live channel: a component (re-)resolves a name over its kept-alive bootstrap channel
// (RESOLVE_OP + tag), and the supervisor - the one process that spawns every service,
// holds its factory root, and runs the restart ladder - answers with a fresh client
// channel to the CURRENT live instance, minted through the service's CONNECT_OP
// factory. A resolve is answered only if the requester's grant set carries that name
// (the capability discipline enforced on every connect, not just once at spawn), and
// the supervisor is single-threaded, so a resolve aimed at a service it is currently
// restarting is simply answered once the replacement serves - the client experiences
// the crash as latency, not an error.

// The live serve roots the broker mints resolved connections from, updated when a
// restart replaces an instance (the old root died with it). `process` is the
// supervisor's OWN ProcessService connection (minted at bring-up, before the root is
// transferred to the shell), which volume-staged replacements are launched through;
// `storage` is the system-volume root a restarted ConfigService's persistence
// backing is re-minted from.
struct Broker {
	config: u64,
	device: u64,
	process: u64,
	storage: u64,
}

// The grant set of each resolving component: which capability names its resolves may
// answer. The canary is the standing client that proves the broker; PermissionManager
// re-resolves the grantable clients it mints for governed tools (`config` / `set` /
// `lsdev`); ConsoleService re-resolves its per-VT config/device factories;
// SystemGraphService re-resolves its device-nodes connection. A component migrating
// to the SvcTransport / connect_or_resolve wrappers adds its row here.
fn cap_grants(requester: &[u8]) -> &'static [&'static [u8]] {
	match requester {
		b"watchdog_probe" => &[CAP_CONFIG],
		b"permission_manager" => &[CAP_CONFIG, CAP_DEVICE],
		b"console_service" => &[CAP_CONFIG, CAP_DEVICE],
		b"system_graph_service" => &[CAP_DEVICE],
		_ => &[],
	}
}

// The manifest service serving a resolvable capability name.
fn service_of_cap(name: &[u8]) -> Option<&'static [u8]> {
	match name {
		CAP_CONFIG => Some(b"config_service"),
		CAP_DEVICE => Some(b"device_service"),
		_ => None,
	}
}

// Answer one RESOLVE request from `requester` arriving on `chan`: check the grant,
// check the service is alive, and mint a fresh client connection from its live root.
// The reply is b"OK" + the minted channel, or b"DENIED" / b"DOWN" with no handle.
unsafe fn serve_resolve(chan: u64, requester: &[u8], request: &[u8], broker: &Broker, state: &[State; N]) {
	unsafe {
		let name: &[u8] = &request[2..];
		if !cap_grants(requester).iter().any(|&g| g == name) {
			send_blocking(chan, b"DENIED", 0);
			return;
		}
		let root: u64 = match name {
			CAP_CONFIG => broker.config,
			CAP_DEVICE => broker.device,
			_ => 0,
		};
		let alive: bool = match service_of_cap(name).and_then(index_of) {
			Some(idx) => state[idx] == State::Running,
			None => false,
		};
		if root == 0 || !alive {
			send_blocking(chan, b"DOWN", 0);
			return;
		}
		match service_connect(root) {
			Some(minted) => {
				send_blocking(chan, b"OK", minted);
			}
			None => {
				send_blocking(chan, b"DOWN", 0);
			}
		}
	}
}

// Whether a RESOLVE request opens `request` (the reserved broker opcode).
fn is_resolve(request: &[u8]) -> bool {
	request.len() > 2 && u16::from_le_bytes([request[0], request[1]]) == RESOLVE_OP
}

// Whether `request` is a service's clean-exit announcement (the shell on logout). A
// service that sends this before closing its report channel is stopping deliberately,
// so the supervisor records a stop rather than a crash.
fn is_goodbye(request: &[u8]) -> bool {
	request.len() >= 2 && u16::from_le_bytes([request[0], request[1]]) == GOODBYE_OP
}

// Restart a crashed service under the Transparent policy - the ladder a real service
// follows (the Tier-1 shape: a self-contained bootstrap, a volume launch plus a fresh
// serve root, so a replacement serves correctly from scratch). Records the crash,
// reaps the dead endpoints (control, Process, the broker's dead root), and - unless
// the restart budget is spent - backs off, relaunches from the volume through the
// supervisor's own ProcessService connection, re-runs the SERVE bootstrap, and waits
// for the replacement's online report. On success the control channel, Process
// handle, broker root and state are all replaced and one restart is charged; on
// failure the service is left Failed (the caller escalates). Clients holding
// channels to the dead instance reconnect through the broker (serve_resolve above) -
// that is the whole point.
unsafe fn restart_service(broker: &mut Broker, idx: usize, state: &mut [State; N], channels: &mut [u64; N], procs: &mut [u64; N], sup: &mut [Supervised; N], budget: u32, park: u64, buf: &mut [u8]) -> bool {
	unsafe {
		sup[idx].failure = Failure::Crashed;
		state[idx] = State::Failed;
		// Reap every endpoint of the dead instance: the control channel, our Process
		// handle, and the broker's serve root (it died with the instance).
		drain_closed(channels[idx], buf);
		if channels[idx] != 0 {
			close(channels[idx]);
			channels[idx] = 0;
		}
		if procs[idx] != 0 {
			close(procs[idx]);
			procs[idx] = 0;
		}
		let root: &mut u64 = match MANIFEST[idx].name {
			b"config_service" => &mut broker.config,
			b"device_service" => &mut broker.device,
			// A Transparent service whose bootstrap this ladder cannot re-run yet.
			_ => return false,
		};
		if *root != 0 {
			close(*root);
			*root = 0;
		}
		// Spend from the restart budget; once exhausted, escalate rather than restart.
		if sup[idx].restarts >= budget {
			return false;
		}
		sleep_ticks(park, RESTART_BACKOFF_TICKS * (sup[idx].restarts as u64 + 1));
		// Relaunch from the volume and re-run the SERVE bootstrap, like bring-up.
		let (manager_side, service_side): (u64, u64) = match channel() {
			Some(pair) => pair,
			None => return false,
		};
		let proc: i64 = launch_from_volume(broker.process, MANIFEST[idx].name, service_side);
		if proc < 0 {
			return false;
		}
		// A restarted ConfigService gets a fresh persistence backing (the old volume
		// connection died with the crashed instance), so the replacement reloads the
		// persisted tree - a `config set` survives the restart.
		if MANIFEST[idx].name == b"config_service" && broker.storage != 0 && !send_factory(manager_side, b"STORAGE", broker.storage) {
			close(proc as u64);
			return false;
		}
		if !bootstrap_serve(manager_side, root) {
			close(proc as u64);
			return false;
		}
		match recv_blocking(manager_side, buf) {
			Received::Message { .. } => {}
			Received::Closed => {
				close(proc as u64);
				return false;
			}
		}
		channels[idx] = manager_side;
		procs[idx] = proc as u64;
		state[idx] = State::Running;
		sup[idx].restarts += 1;
		sup[idx].failure = Failure::None;
		true
	}
}

// Drive one canary CHECK to its verdict: send the command, then serve the RESOLVE
// requests the canary's client machinery sends back on the same channel until its
// non-resolve reply (the verdict) arrives. Both sides are single-threaded, so the
// interleaving is deterministic. Returns the verdict length in `buf` (0 = the canary
// died mid-check).
unsafe fn drive_check(canary_ctrl: u64, broker: &Broker, state: &[State; N], buf: &mut [u8]) -> usize {
	unsafe {
		if !send_blocking(canary_ctrl, b"CHECK", 0) {
			return 0;
		}
		loop {
			match recv_blocking(canary_ctrl, buf) {
				Received::Message { len, .. } if is_resolve(&buf[..len]) => {
					// Copy the request out of `buf`: serve_resolve round-trips reuse it.
					let mut req: [u8; 64] = [0u8; 64];
					let rlen: usize = len.min(req.len());
					req[..rlen].copy_from_slice(&buf[..rlen]);
					serve_resolve(canary_ctrl, b"watchdog_probe", &req[..rlen], broker, state);
				}
				Received::Message { len, .. } => return len,
				Received::Closed => return 0,
			}
		}
	}
}

// A proto Transport for the drill's PermissionManager round-trip. The supervisor is
// both the CALLER of the `run` op and the BROKER PermissionManager re-resolves its
// dead config grant through mid-request - a plain blocking call would deadlock the
// two single-threaded processes - so this transport serves the RESOLVE requests
// arriving on PermissionManager's control channel while it waits for the typed
// reply. (Only the drill wires this cycle; live callers - the shell, the console -
// are not the broker, so their calls stay plain ChannelTransport.)
struct DrillTransport<'a> {
	perm: u64,
	pm_ctrl: u64,
	broker: &'a Broker,
	state: &'a [State; N],
}

impl proto::codec::Transport for DrillTransport<'_> {
	fn call(&mut self, request: &[u8], request_handle: u64) -> Option<(Vec<u8>, u64)> {
		unsafe {
			if !send_blocking(self.perm, request, request_handle) {
				return None;
			}
			let mut buf: [u8; 64] = [0u8; 64];
			loop {
				let pair: [u64; 2] = [self.perm, self.pm_ctrl];
				match wait_any(&pair, 0) {
					1 => {
						if let Polled::Message { len, .. } = try_recv(self.pm_ctrl, &mut buf) {
							if is_resolve(&buf[..len]) {
								serve_resolve(self.pm_ctrl, b"permission_manager", &buf[..len], self.broker, self.state);
							}
						}
					}
					0 => match recv_vec_blocking(self.perm) {
						ReceivedVec::Message { bytes, handle } => return Some((bytes, handle)),
						ReceivedVec::Closed => return None,
					},
					_ => return None,
				}
			}
		}
	}
}

// Sleep for `ticks` by waiting on the never-written `park` channel until the deadline
// passes. A bounded one-shot wait that sleeps the thread at ~0% CPU; the test scheduler
// advances the finite deadline, so the sleep is deterministic under test.
unsafe fn sleep_ticks(park: u64, ticks: u64) {
	unsafe {
		if park == 0 {
			return;
		}
		wait(park, clock() + ticks);
	}
}

// Stand as the supervisor after bring-up. Each iteration builds a wait set from every
// live control channel - the Running services' (kind 0), the canary's (kind 1), the
// shell's admin channel (kind 2), and SystemGraphService's stats channel (kind 3) - and
// blocks on all of them at once with no deadline, so the supervisor sleeps at ~0% CPU
// until one needs attention. A control channel MESSAGE is a broker RESOLVE (a component
// re-resolving a granted service name - answered with a fresh connection to the live
// instance); a service or the canary peer-closing means it crashed. A crashed service
// under the transparent restart policy (ConfigService) is restarted per the ladder and
// its clients reconnect through the broker; any other service is recorded Failed and
// dropped from the wait set. The canary is restarted per policy; an admin message
// drives a reverse-dependency stop; a stats request is answered over the `supervisor`
// interface. Returns when nothing is left to watch.
unsafe fn supervise(state: &mut [State; N], channels: &mut [u64; N], sup: &mut [Supervised; N], reason: &[String; N], procs: &mut [u64; N], package: &Package, broker: &mut Broker, canary_proc: &mut u64, canary_ctrl: &mut u64, canary_sup: &mut Supervised, policy: &Policy, admin_server: u64, admin_server2: u64, stats_server: u64, stats_server2: u64, drivers: &[(&'static [u8], bool)], log_client: u64, up: u64, park: u64, buf: &mut [u8]) {
	unsafe {
		let mut admin: u64 = admin_server;
		let mut admin2: u64 = admin_server2;
		let mut stats: u64 = stats_server;
		let mut stats2: u64 = stats_server2;
		loop {
			let mut handles: [u64; N + 5] = [0u64; N + 5];
			let mut kinds: [u8; N + 5] = [0u8; N + 5];
			let mut idxs: [usize; N + 5] = [0usize; N + 5];
			let mut count: usize = 0;
			let mut i: usize = 0;
			while i < N {
				if state[i] == State::Running && channels[i] != 0 {
					handles[count] = channels[i];
					kinds[count] = 0;
					idxs[count] = i;
					count += 1;
				}
				i += 1;
			}
			if *canary_ctrl != 0 {
				handles[count] = *canary_ctrl;
				kinds[count] = 1;
				count += 1;
			}
			if admin != 0 {
				handles[count] = admin;
				kinds[count] = 2;
				count += 1;
			}
			if stats != 0 {
				handles[count] = stats;
				kinds[count] = 3;
				count += 1;
			}
			if admin2 != 0 {
				handles[count] = admin2;
				kinds[count] = 4;
				count += 1;
			}
			if stats2 != 0 {
				handles[count] = stats2;
				kinds[count] = 5;
				count += 1;
			}
			if count == 0 {
				return;
			}
			let ready: i64 = wait_any(&handles[..count], 0);
			if ready < 0 {
				return;
			}
			let r: usize = ready as usize;
			match kinds[r] {
				0 => {
					// A real service's control channel fired: a message is a broker RESOLVE
					// (answered with a fresh connection); a peer-close means it crashed - a
					// service under the transparent restart policy is restarted per the
					// ladder (its clients reconnect through the broker), any other is
					// recorded Failed and dropped from the wait set.
					let idx: usize = idxs[r];
					match try_recv(channels[idx], buf) {
						Polled::Message { len, .. } if is_resolve(&buf[..len]) => {
							// Copy the request out of `buf`: serve_resolve reuses it.
							let mut req: [u8; 64] = [0u8; 64];
							let rlen: usize = len.min(req.len());
							req[..rlen].copy_from_slice(&buf[..rlen]);
							serve_resolve(channels[idx], MANIFEST[idx].name, &req[..rlen], broker, state);
						}
						Polled::Message { len, .. } if is_goodbye(&buf[..len]) => {
							// A deliberate exit (the shell on logout), not a crash: record a
							// clean stop and drop it from the wait set. ConsoleService reloads a
							// fresh shell on the VT, so nothing here restarts it.
							emit_event(log_client, MANIFEST[idx].name, b"exited");
							state[idx] = State::Stopped;
							channels[idx] = 0;
						}
						Polled::Closed => {
							emit_event(log_client, MANIFEST[idx].name, b"crashed");
							console_report(MANIFEST[idx].name, b"crashed");
							if MANIFEST[idx].restart == Restart::Transparent {
								if restart_service(broker, idx, state, channels, procs, sup, policy.restart_budget, park, buf) {
									emit_event(log_client, MANIFEST[idx].name, b"restarted");
									console_report(MANIFEST[idx].name, b"restarted");
								} else {
									emit_event(log_client, MANIFEST[idx].name, b"escalated");
									console_report(MANIFEST[idx].name, b"escalated");
								}
							} else {
								state[idx] = State::Failed;
								sup[idx].failure = Failure::Crashed;
								channels[idx] = 0;
							}
						}
						_ => {}
					}
				}
				1 => {
					// The canary's control channel fired: a message is a broker RESOLVE (the
					// canary is a standing client too); a peer-close means it crashed, so
					// restart it per policy (escalating once the budget is spent).
					match try_recv(*canary_ctrl, buf) {
						Polled::Message { len, .. } if is_resolve(&buf[..len]) => {
							let mut req: [u8; 64] = [0u8; 64];
							let rlen: usize = len.min(req.len());
							req[..rlen].copy_from_slice(&buf[..rlen]);
							serve_resolve(*canary_ctrl, b"watchdog_probe", &req[..rlen], broker, state);
						}
						Polled::Closed => {
							if restart_canary(package, canary_proc, canary_ctrl, canary_sup, Failure::Crashed, policy.restart_budget, park, buf) {
								emit_event(log_client, b"watchdog_probe", b"restarted");
								console_report(b"watchdog_probe", b"restarted");
							} else {
								emit_event(log_client, b"watchdog_probe", b"escalated");
								console_report(b"watchdog_probe", b"escalated");
							}
						}
						_ => {}
					}
				}
				2 => {
					// The shell asked to stop a service; tear down its dependents first.
					if !handle_admin(admin, state, channels, sup, procs, log_client, up, buf) {
						admin = 0;
					}
				}
				3 => {
					// SystemGraphService queried the supervisor state; answer one request.
					if !serve_stats_once(stats, state, sup, reason, canary_sup, drivers, buf) {
						stats = 0;
					}
				}
				4 => {
					// The sandboxed `stop` tool (granted the supervisor capability) asked to
					// stop a service over its own admin channel; tear down its dependents first.
					if !handle_admin(admin2, state, channels, sup, procs, log_client, up, buf) {
						admin2 = 0;
					}
				}
				_ => {
					// The sandboxed `lssvc` tool (granted the services capability) queried the
					// supervisor state over its own status channel; answer one request.
					if !serve_stats_once(stats2, state, sup, reason, canary_sup, drivers, buf) {
						stats2 = 0;
					}
				}
			}
		}
	}
}

// Handle one admin request from the shell: the bare name of a service to stop. An
// unknown name (or one already down) gets a NOTFOUND reply; otherwise the service and
// its dependents are torn down and the newline-joined list of what stopped is replied
// for the shell to print. Returns false once the admin channel's peer (the shell) is
// gone, so the supervisor drops it from its wait set.
unsafe fn handle_admin(admin: u64, state: &mut [State; N], channels: &mut [u64; N], sup: &mut [Supervised; N], procs: &[u64; N], log_client: u64, up: u64, buf: &mut [u8]) -> bool {
	unsafe {
		let len: usize = match recv_blocking(admin, buf) {
			Received::Message { len, .. } => len,
			Received::Closed => return false,
		};
		// Copy the name out of `buf`, since the teardown reuses it to drain control channels.
		let mut namebuf: [u8; 64] = [0u8; 64];
		let nlen: usize = len.min(namebuf.len()).min(buf.len());
		namebuf[..nlen].copy_from_slice(&buf[..nlen]);
		let name: &[u8] = &namebuf[..nlen];
		// A power verb (the shell's graceful `poweroff` / `reboot`): the reserved names
		// `!poweroff` / `!reboot` (a real service name can never start with `!`) mean tear
		// the whole service tree down in reverse-dependency order - flushing LogService's
		// last journal batch first - then power the machine off (or reboot) from here, so no
		// service is killed while a dependent still needs it. system_power does not return;
		// if it somehow does (an unsupported machine), the loop stays alive.
		if name == b"!poweroff" || name == b"!reboot" {
			let action: u64 = if name == b"!reboot" { POWER_REBOOT } else { POWER_OFF };
			shutdown_all(state, channels, sup, procs, log_client, buf);
			system_power(action);
			return true;
		}
		match index_of(name) {
			Some(target) if state[target] == State::Running => {
				let stopped: Vec<u8> = stop_subtree(target, state, channels, sup, procs, log_client, up, buf);
				let mut reply: Vec<u8> = Vec::new();
				reply.extend_from_slice(b"STOPPED\n");
				reply.extend_from_slice(&stopped);
				send_blocking(admin, &reply, 0);
			}
			_ => {
				send_blocking(admin, b"NOTFOUND", 0);
			}
		}
		true
	}
}

// Tear down a service and every component that transitively depends on it, dependents
// first. The scope is the target plus its reverse-dependency closure, minus the shell
// (the controlling terminal is exempt: force-killing the issuing client is hostile, and
// its Process handle is not held here anyway). Components are stopped in reverse-
// topological order - repeatedly a current leaf of the scoped subgraph, one nothing in
// scope still depends on - so a dependent always stops before its dependency. A service
// is stopped by killing its process (services serve on their SERVE channels and ignore
// their bootstrap, so the cooperative STOP protocol does not reach them) and draining
// its control channel to the peer-close. Returns the newline-joined names of everything
// stopped, in teardown order.
unsafe fn stop_subtree(target: usize, state: &mut [State; N], channels: &mut [u64; N], sup: &mut [Supervised; N], procs: &[u64; N], log_client: u64, up: u64, buf: &mut [u8]) -> Vec<u8> {
	unsafe {
		let mut scope: [bool; N] = [false; N];
		scope[target] = true;
		// Fixpoint: add any Running component that depends on something already in scope,
		// until nothing new is added - the full reverse-dependency closure of the target.
		loop {
			let mut grew: bool = false;
			let mut i: usize = 0;
			while i < N {
				if !scope[i] && state[i] == State::Running && depends_on_scoped(i, &scope) {
					scope[i] = true;
					grew = true;
				}
				i += 1;
			}
			if !grew {
				break;
			}
		}
		// Exempt the shell: it transitively depends on everything, so it would always be in
		// scope, but the issuing terminal must survive (and procs[shell] == 0 regardless).
		if let Some(sh) = index_of(b"shell") {
			scope[sh] = false;
		}
		let mut stopped: Vec<u8> = Vec::new();
		loop {
			let mut progress: bool = false;
			let mut i: usize = 0;
			while i < N {
				if scope[i] && state[i] == State::Running && !has_running_dependent(i, &scope, state) {
					if procs[i] != 0 {
						signal(procs[i], SIG_KILL);
					}
					drain_closed(channels[i], buf);
					if channels[i] != 0 {
						close(channels[i]);
						channels[i] = 0;
					}
					state[i] = State::Stopped;
					sup[i].failure = Failure::Stopped;
					emit_event(log_client, MANIFEST[i].name, b"stopped");
					console_report(MANIFEST[i].name, b"stopped");
					if !stopped.is_empty() {
						stopped.push(b'\n');
					}
					stopped.extend_from_slice(MANIFEST[i].name);
					progress = true;
				}
				i += 1;
			}
			if !progress {
				break;
			}
		}
		let _ = up;
		stopped
	}
}

// Whether component `i` depends on any component currently in the teardown scope.
fn depends_on_scoped(i: usize, scope: &[bool; N]) -> bool {
	for &dep in MANIFEST[i].deps {
		if let Some(d) = index_of(dep) {
			if scope[d] {
				return true;
			}
		}
	}
	false
}

// Whether any in-scope Running component still depends on component `i` - i.e. `i` is
// not yet a leaf of the scoped subgraph and must not be stopped this round.
fn has_running_dependent(i: usize, scope: &[bool; N], state: &[State; N]) -> bool {
	let mut j: usize = 0;
	while j < N {
		if j != i && scope[j] && state[j] == State::Running && index_of_dep(j, i) {
			return true;
		}
		j += 1;
	}
	false
}

// Whether component `j` declares component `i` among its dependencies.
fn index_of_dep(j: usize, i: usize) -> bool {
	for &dep in MANIFEST[j].deps {
		if index_of(dep) == Some(i) {
			return true;
		}
	}
	false
}

// The reverse-dependency teardown order for a graceful shutdown: every currently
// Running service (the shell exempted - it is the issuing terminal and holds no
// supervised Process here), ordered so a dependent always precedes every dependency it
// declares. Computed by repeatedly taking the current leaves of the scoped subgraph (a
// service nothing not-yet-dropped still depends on), the same rule stop_subtree tears
// down by. Pure - it computes the order, it stops nothing - so the same function drives
// the live `shutdown_all` teardown and is checked by the selftest ordering drill. A
// dependency cycle would leave some service never becoming a leaf, so it would be
// absent from the returned order (which the selftest verifies against).
fn shutdown_order(state: &[State; N]) -> Vec<usize> {
	let mut scope: [bool; N] = [false; N];
	let mut i: usize = 0;
	while i < N {
		if state[i] == State::Running {
			scope[i] = true;
		}
		i += 1;
	}
	if let Some(sh) = index_of(b"shell") {
		scope[sh] = false;
	}
	let mut dropped: [bool; N] = [false; N];
	let mut order: Vec<usize> = Vec::new();
	loop {
		let mut progress: bool = false;
		let mut i: usize = 0;
		while i < N {
			if scope[i] && !dropped[i] && !has_scoped_undropped_dependent(i, &scope, &dropped) {
				dropped[i] = true;
				order.push(i);
				progress = true;
			}
			i += 1;
		}
		if !progress {
			break;
		}
	}
	order
}

// Whether any in-scope, not-yet-dropped component still depends on component `i` - i.e.
// `i` is not yet a leaf of the shrinking scoped subgraph, so it must not be dropped this
// round (its dependents must go first). The `shutdown_order` counterpart of
// `has_running_dependent`, tracking dropped-so-far instead of live state.
fn has_scoped_undropped_dependent(i: usize, scope: &[bool; N], dropped: &[bool; N]) -> bool {
	let mut j: usize = 0;
	while j < N {
		if j != i && scope[j] && !dropped[j] && index_of_dep(j, i) {
			return true;
		}
		j += 1;
	}
	false
}

// Tear the whole service tree down for a graceful power-off, then return so the caller
// powers the machine off. First ask LogService to flush its last journal batch (it is a
// dependency of nearly everything, so it stops near-last; sending now gives it ample
// scheduling before its own kill). Then stop every Running service in reverse-dependency
// order (from `shutdown_order`, so a dependent always stops before its dependency) by
// killing its process and draining its control channel to the peer-close - the same
// per-service teardown as `stop_subtree`. The shell (the issuing terminal) is exempt:
// `shutdown_order` omits it, so it is never drained (which would block) and it dies with
// the machine at power-off.
unsafe fn shutdown_all(state: &mut [State; N], channels: &mut [u64; N], sup: &mut [Supervised; N], procs: &[u64; N], log_client: u64, buf: &mut [u8]) {
	unsafe {
		if let Some(log) = index_of(b"log_service") {
			if state[log] == State::Running && channels[log] != 0 {
				send_blocking(channels[log], b"FLUSH", 0);
			}
		}
		let order: Vec<usize> = shutdown_order(state);
		for &idx in &order {
			if state[idx] != State::Running {
				continue;
			}
			if procs[idx] != 0 {
				signal(procs[idx], SIG_KILL);
			}
			drain_closed(channels[idx], buf);
			if channels[idx] != 0 {
				close(channels[idx]);
				channels[idx] = 0;
			}
			state[idx] = State::Stopped;
			sup[idx].failure = Failure::Stopped;
			emit_event(log_client, MANIFEST[idx].name, b"stopped");
			console_report(MANIFEST[idx].name, b"stopped");
		}
	}
}

// Verify a computed shutdown order (the selftest ordering drill): it must cover every
// Running non-shell service (a dependency cycle would strand one out of the order), and
// list each service before every dependency it declares that is also in the order (a
// dependent must tear down before its dependency). Non-destructive.
fn verify_shutdown_order(order: &[usize], state: &[State; N]) -> bool {
	let shell: Option<usize> = index_of(b"shell");
	let mut i: usize = 0;
	while i < N {
		if state[i] == State::Running && Some(i) != shell && !order.contains(&i) {
			return false;
		}
		i += 1;
	}
	let mut pos: usize = 0;
	while pos < order.len() {
		let x: usize = order[pos];
		for &dep in MANIFEST[x].deps {
			if let Some(d) = index_of(dep) {
				if let Some(dpos) = order.iter().position(|&s| s == d) {
					if dpos < pos {
						return false;
					}
				}
			}
		}
		pos += 1;
	}
	true
}

// Answer one request on a supervisor stats channel (SystemGraphService's, or the
// sandboxed `lssvc` tool's): decode it, build the per-component status list (state,
// restarts, watchdog trips, last failure for each manifest service, the canary, and
// each driver), and reply over the `supervisor` interface. Returns false once the
// channel's peer is gone, so the supervisor drops it from its wait set.
unsafe fn serve_stats_once(stats: u64, state: &[State; N], sup: &[Supervised; N], reason: &[String; N], canary_sup: &Supervised, drivers: &[(&'static [u8], bool)], buf: &mut [u8]) -> bool {
	unsafe {
		let (len, handle): (usize, u64) = match recv_blocking(stats, buf) {
			Received::Message { len, handle } => (len, handle),
			Received::Closed => return false,
		};
		let mut api = StatsApi { state, sup, reason, canary_sup, drivers };
		let mut reply: [u8; 4096] = [0u8; 4096];
		let mut reply_handle: u64 = 0;
		if let Some(n) = supervisor::dispatch(&mut api, &buf[..len], handle, &mut reply, &mut reply_handle) {
			send_blocking(stats, &reply[..n], reply_handle);
		}
		true
	}
}

// The name of a service's lifecycle state, as the status view reports it.
fn state_name(state: State) -> &'static str {
	match state {
		State::Pending => "pending",
		State::Running => "running",
		State::Stopped => "stopped",
		State::Failed => "failed",
	}
}

// The supervisor's view of its own bookkeeping, served over the `supervisor` interface.
struct StatsApi<'a> {
	state: &'a [State; N],
	sup: &'a [Supervised; N],
	reason: &'a [String; N],
	canary_sup: &'a Supervised,
	drivers: &'a [(&'static [u8], bool)],
}

impl<'a> supervisor::Service for StatsApi<'a> {
	fn status(&mut self) -> Result<Vec<SupervisorStat>, Error> {
		let mut out: Vec<SupervisorStat> = Vec::new();
		let mut i: usize = 0;
		while i < N {
			// A service that failed to bootstrap carries the reason it reported (the failing
			// step and error); a still-standing service has none, so fall back to the failure
			// class (crashed / hung / stopped) the supervisor last recorded.
			let last_failure: String = if self.reason[i].is_empty() { String::from_utf8_lossy(self.sup[i].failure.as_bytes()).into_owned() } else { self.reason[i].clone() };
			out.push(SupervisorStat { name: String::from_utf8_lossy(MANIFEST[i].name).into_owned(), state: String::from(state_name(self.state[i])), restarts: self.sup[i].restarts, watchdog_trips: self.sup[i].watchdog_trips, last_failure });
			i += 1;
		}
		out.push(SupervisorStat { name: String::from("watchdog_probe"), state: String::from("running"), restarts: self.canary_sup.restarts, watchdog_trips: self.canary_sup.watchdog_trips, last_failure: String::from_utf8_lossy(self.canary_sup.failure.as_bytes()).into_owned() });
		// Drivers are services too: DeviceManager launches them and each hands its channel
		// over when it reports in, so a driver whose channel arrived is running and one
		// that never reported is pending (absent hardware, or not launched this boot).
		for &(name, online) in self.drivers {
			out.push(SupervisorStat { name: String::from_utf8_lossy(name).into_owned(), state: String::from(if online { "running" } else { "pending" }), restarts: 0, watchdog_trips: 0, last_failure: String::new() });
		}
		Ok(out)
	}
}
